package foodbank.inventory.entity;

import java.util.List;
import java.util.Set;

public class ManualForm {

	/** 
	 * TODO: Create the real donor objects
	 * **/
	// Set object used to ensure unique values stored only
	Set<String> donorSet;
	Set<String> weightGSet;
	Set<String> weightLSet;
	List<Category> categoryList;
	
	public ManualForm(Set<String> donorSet, Set<String> weightGSet, Set<String> weightLSet, List<Category> categoryList) {
		this.donorSet = donorSet;
		this.weightGSet = weightGSet;
		this.weightLSet = weightLSet;
		this.categoryList = categoryList;
	}

	public Set<String> getDonorSet() {
		return donorSet;
	}

	public void setDonorSet(Set<String> donorSet) {
		this.donorSet = donorSet;
	}

	public Set<String> getWeightGSet() {
		return weightGSet;
	}

	public void setWeightGSet(Set<String> weightGSet) {
		this.weightGSet = weightGSet;
	}

	public Set<String> getWeightLSet() {
		return weightLSet;
	}

	public void setWeightLSet(Set<String> weightLSet) {
		this.weightLSet = weightLSet;
	}

	public List<Category> getCategoryList() {
		return categoryList;
	}

	public void setCategoryList(List<Category> categoryList) {
		this.categoryList = categoryList;
	}
	
}
